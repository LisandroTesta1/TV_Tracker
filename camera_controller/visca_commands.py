CAM_ColorEnhance_cmd = {
    'Parameter_Set': (0x01, 0x04, 0x20), # Add (mm nn pp qq rr),
                                         # mm: First byte from the top threshold value =(0x0-0xF)
                                         # nn: Second byte from the top threshold value =(0x0-0xF)
                                         # pp: Third byte from the top threshold value =(0x0-0xF)
                                         # qq: Color specification for high-intensity =(0x0-0x8)
                                         # rr: Color specification for low-intensity =(0x0-0x8)
    'On': (0x01, 0x04, 0x50, 0x02),
    'Off': (0x01, 0x04, 0x50, 0x03),
    }


